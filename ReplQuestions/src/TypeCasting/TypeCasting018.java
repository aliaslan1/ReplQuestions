package TypeCasting;

public class TypeCasting018 {

//	Write a program to convert double 500.4444 into long.


	public static void main (String [] args) {
	    
	    double d = 500.4444 ;         
	        //explicit type casting 
	        long l = (long)d;  
	        System.out.println(l);
	    
	  } 
	  
	}