package ArithmeticOperation;

public class ArithmeticOperation009 {

//	Create a float data type variable and assign a value of  8.2 to it
//	Multiply the value by itself and print the result.

	public static void main(String[] args) {
		float variable = 8.2f;
		float mult = variable * variable;
		System.out.println("The multiplication of " + variable + " value with itself is equal to " + mult);
	}
}
